function mostrarcli() {
    var usu = document.getElementById("usu");
    usu.style.display = "none";
	var cli = document.getElementById("cli");
    cli.style.display = "block";
	var ven = document.getElementById("ven");
    vene.style.display = "none";
    return true;
}
